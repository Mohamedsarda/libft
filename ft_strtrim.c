#include "libft.h"

char	*ft_strtrim(char const *s1, char const *set)
{
	int		end;
	char	*str;
	size_t	len;

	if (!s1 || !set)
		return (NULL);
	while (*s1 && ft_strchr(set, *s1))
		s1++;
	end = ft_strlen(s1);
	while (end && ft_strchr(set, s1[end - 1]))
		end--;
	str = (char *)malloc((end + 1) * sizeof(char));
	if (!str)
		return (NULL);
	len = end + 1;
	ft_strlcpy(str, s1, len);
	return (str);
}
